<?php
/**
 * Vendors the kilden/kilden-php core under includes/vendor-kilden with the
 * namespace prefixed to KildenWP\Vendor\Kilden, so the plugin can never
 * collide with another plugin shipping the same SDK.
 *
 * Why not PHP-Scoper: the core is a single-namespace, zero-dependency
 * library — a deterministic textual rewrite of `Kilden\` covers it
 * completely and keeps the build dependency-free. Revisit if the core ever
 * grows dependencies.
 *
 * Usage: php bin/build-vendor.php [path-to-kilden-sdk-php]
 */

$source = isset($argv[1]) ? rtrim($argv[1], '/') : dirname(__DIR__, 2) . '/kilden-sdk-php';
$sourceSrc = $source . '/src';
$target = dirname(__DIR__) . '/includes/vendor-kilden';

if (!is_dir($sourceSrc)) {
    fwrite(STDERR, "core not found at {$sourceSrc}\n");
    exit(1);
}

// Clean rebuild.
if (is_dir($target . '/src')) {
    $it = new RecursiveIteratorIterator(
        new RecursiveDirectoryIterator($target . '/src', FilesystemIterator::SKIP_DOTS),
        RecursiveIteratorIterator::CHILD_FIRST
    );
    foreach ($it as $file) {
        $file->isDir() ? rmdir($file->getPathname()) : unlink($file->getPathname());
    }
}

$classmap = array();
$files = new RecursiveIteratorIterator(new RecursiveDirectoryIterator($sourceSrc, FilesystemIterator::SKIP_DOTS));
foreach ($files as $file) {
    if ($file->getExtension() !== 'php') {
        continue;
    }
    $relative = substr($file->getPathname(), strlen($sourceSrc) + 1);
    $code = file_get_contents($file->getPathname());

    $code = preg_replace('/^namespace Kilden(\\\\[^;]+)?;/m', 'namespace KildenWP\\Vendor\\Kilden$1;', $code);
    $code = preg_replace('/^use Kilden\\\\/m', 'use KildenWP\\Vendor\\Kilden\\', $code);
    // Fully-qualified references in code (rare but possible); the negative
    // lookbehind keeps the rewrite idempotent on already-prefixed lines.
    $code = preg_replace('/(?<!Vendor)\\\\Kilden\\\\(Transport|Internal|FeatureFlags)\\\\/', '\\\\KildenWP\\\\Vendor\\\\Kilden\\\\$1\\\\', $code);

    $dest = $target . '/src/' . $relative;
    if (!is_dir(dirname($dest))) {
        mkdir(dirname($dest), 0755, true);
    }
    file_put_contents($dest, $code);

    if (preg_match('/^namespace (KildenWP\\\\Vendor\\\\Kilden(?:\\\\[^;]+)?);/m', $code, $ns)
        && preg_match('/^(?:final |abstract )?(?:class|interface|trait) (\w+)/m', $code, $cls)) {
        $classmap[$ns[1] . '\\' . $cls[1]] = 'src/' . str_replace('\\', '/', $relative);
    }
}

ksort($classmap);
$entries = '';
foreach ($classmap as $fqcn => $path) {
    $entries .= sprintf("    %s => __DIR__ . %s,\n", var_export($fqcn, true), var_export('/' . $path, true));
}

file_put_contents($target . '/autoload.php', <<<PHP
<?php
/**
 * Generated by bin/build-vendor.php — do not edit. Classmap autoloader for
 * the vendored, namespace-prefixed kilden/kilden-php core.
 */

\$kilden_wp_classmap = array(
{$entries});

spl_autoload_register(static function (\$class) use (\$kilden_wp_classmap) {
    if (isset(\$kilden_wp_classmap[\$class])) {
        require \$kilden_wp_classmap[\$class];
    }
});

PHP);

echo 'vendored ' . count($classmap) . " classes from {$sourceSrc}\n";
